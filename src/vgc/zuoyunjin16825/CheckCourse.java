    public void showExamData() {
        ArrayList<examData> list = examList();
        DefaultTableModel model = (DefaultTableModel) CourseTable.getModel();
        Object[] row = new Object[1];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getCNA();
            model.addRow(row);
        }
    }
     
