    String UserID;

    public void getUserID() {
        try {

            FileReader file2 = new FileReader("UserID.txt");
            BufferedReader buffer = new BufferedReader(file2);
            String line;
            while ((line = buffer.readLine()) != null) {
                UserID = line;
            }
            buffer.close();
        } catch (IOException e) {
            System.out.println("A write error has occured");
        }

    }
    
    public ArrayList<examData> examList() {
        getUserID();
        ArrayList<examData> examList = new ArrayList();
        Connection myConn = getConnection();
        String sql = "select Courses.CoursesName from ExamsGradeBook inner join Courses on ExamsGradeBook.CoursesID = Courses.CoursesID where ExamsGradeBook.StudentID =" + UserID+" Group by CoursesName";
        try {
            Statement myStmt = myConn.createStatement();
            ResultSet myRs = myStmt.executeQuery(sql);
            examData examDataTable;
            while (myRs.next()) {

                examDataTable = new examData(myRs.getString("CoursesName"));
                examList.add(examDataTable);
            }
        } catch (Exception exc) {
            exc.printStackTrace();
        }
        return examList;
    }

